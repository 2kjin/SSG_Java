package day2;

public class FourArithmetic {
    public static void main(String[] args) {
        int a = 12;
        int b = 2;

//        System.out.printf("%d + %d = % d\n", a, b, a+b);
        System.out.printf("a + b = %d\n", a+b);
        System.out.printf("a - b = %d\n", a-b);
        System.out.printf("a * b = %d\n", a*b);
        System.out.printf("a / b = %d\n", a/b);
    }
}
